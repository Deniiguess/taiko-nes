.proc load_title_screen
  ; setup sprite 0 hit
  LDA #$C8
  STA $200
  LDA #$06
  STA $201
  LDA #%00100000
  STA $202

  ; load PRG banks
  LDA #$01
  STA $E000

  ; load CHR banks
  LDA #$00
  STA $8000
  LDA #$05
  STA $A000
  LDA #$06
  STA $A800
  LDA #$07
  STA $B000
  LDA #$02
  STA $B800

  ; load PPU nametables
  LDA #$E1
  STA $C800
  STA $D800
  STA $C000
  STA $D000

  ; define song
  LDX #<(taiko_sfx) ; load low byte to X
  LDY #>(taiko_sfx) ; load high byte to Y

  LDA #$01 ; NTSC speed
  JSR famistudio_init ; initialize songs

  LDA #$01
  STA scene

  LDA misc
  AND #%11111110
  STA misc

  LDX #$00
  load_title_palette:
  LDA title_palette, X
  STA palette, X
  INX
  CPX #$14
  BNE load_title_palette

  LDA #$0F
  STA palette+16

  LDA #$04
  STA fade_intensity

  LDA PPUSTATUS
  LDA #$20
  STA PPUADDR
  LDA #$00
  STA PPUADDR
  LDX #$00

  load_title_bg:
  load_title_bg_1:
  LDA title_bg_1, X
  STA PPUDATA
  INX
  BNE load_title_bg_1

  load_title_bg_2:
  LDA title_bg_2, X
  STA PPUDATA
  INX
  BNE load_title_bg_2

  load_title_bg_3:
  LDA title_bg_3, X
  STA PPUDATA
  INX
  BNE load_title_bg_3

  load_title_bg_4:
  LDA title_bg_4, X
  STA PPUDATA
  INX
  BNE load_title_bg_4

  RTS
.endproc

